#include<iostream>
using namespace std;

int main(){
  // define variabes
  int firstNum, secondNum;
  // Get inputs 
  cout<<endl<<"Enter first number ↠ ";
  cin>>firstNum;
  cout<<endl<<"Enter second number ↠ ";
  cin>>secondNum;
  // do mathematical operations here 
  // Addition
  cout<<endl<<"Addition : "<<firstNum+secondNum;
  // Substraction
  cout<<endl<<"Substraction : "<<firstNum-secondNum;
  // Multiplication 
  cout<<endl<<"Multiplication : "<<firstNum*secondNum;
  // Division 
  cout<<endl<<"Division : "<<firstNum/secondNum;
  return 0;
}
